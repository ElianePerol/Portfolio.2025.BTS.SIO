                <h6 class="blog-title">What is a qubit?</h6>
                
                <!-- header-start -->
                <div class="portfolio-modal-table">
                    <div class="row">
                        <div class="col-md-6">
                            <h3 class="portfolio-modal-table-text">
                                <i class="fa-light fa-file-lines"></i>
