                <!-- projet-content-start -->
                <div class="h1-modal-paragraph">
                    <h5>Contexte</h5>
                        <p>Au GIE MYXLAB, j'ai participé à la rédaction de la Politique de Sécurité des Systèmes d'Information (PSSI) pour remplacer la charte informatique, dans le but de répondre aux exigences de l'audit COFRAC.</p>
                        <p>Ce projet visait à élaborer une PSSI adaptée aux pratiques de l'entreprise et à structurer des plans d'action pour des projets futurs, comme le PCA/PRA.
                        </p>
                        <p><strong>Contrainte principale : </strong>finaliser la PSSI avant l’audit, en utilisant SAPANET pour le suivi et la gestion des plans d’action.</p>
                    
                    <h5>Avec qui :</h5>
                        <p>
                        <ul>
                            <li><b>• Philippe Soulard</b> - RSI, tuteur et pilote du projet</li>
                            <li><b>• Kévin Gandon</b> - Technicien informatique, configuration OCS et GLPI</li>
                        </ul>
                        </p>
                    
                    <h5>Environnement technologique</h5>
                        <p>
                            <h6>Outils utilisés :</h6>
                            <ul>
                                <li><b>• Microsoft Word </b>: rédaction et mise en forme de la PSSI</li>
                                <li><b>• SAPANET </b>: outil qualité pour le suivi des plans d’action et la gestion des avancées.</li>
                                <li><b>• OCS Inventory et GLPI </b>: inventaire automatisé des équipements.</li>
                            </ul>
                        </p>
                        <p>
                            <h6>Ressources et support :</h6>
                            <ul>
                                <li>• Documentation fournie par le prestataire (premier jet de la PSSI).</li>
                                <li>• Ressources sur les exigences COFRAC et les bonnes pratiques en sécurité informatique.</li>
                                <li>• Guide d'élaboration de l'ANSSI</li>
                        </p>

                    <h5>Tâches</h5>
                        <p>
                            <h6>Assistance au RSI dans la rédaction de la PSSI : </h6>
                            <ul>
                                <li>• Étude et adaptation du premier jet fourni par le prestataire.</li>
                                <li>• Traduction des concepts techniques pour garantir une compréhension commune et une intégration cohérente avec les pratiques actuelles de l’entreprise.</li>
                                <li>• Structuration du contenu pour éviter d’inclure des engagements non réalisables ou prématurés, tout en répondant aux critères de l’audit.</li>
                            </ul>
                        </p>
                        <p>
                            <h6>Mise en place de mesures concrètes :</h6>
                            <ul>
                                <li>• Coordination avec le technicien informatique pour configurer et utiliser OCS Inventory et GLPI, créant un inventaire automatisé fiable des équipements.</li>
                                <li>• Introduction, dans la PSSI, de politiques de mots de passe et de mesures de sécurité adaptées au contexte de l’entreprise.</li>
                            </ul>
                        </p>
                        <p>
                            <h6>Suivi et gestion via SAPANET :</h6>
                            <ul>
                                <li>• Création et mise à jour d’un Plan d’Action principal pour la rédaction de la PSSI.</li>
                                <li>• Définition de Plans d’Action complémentaires pour les éléments mentionnés dans la PSSI mais non encore finalisés (PCA/PRA, politique de sauvegarde renforcée).</li>
                            </ul>
                        </p>
                        <p>
                            <h6>Révisions et validation :</h6>
                            <ul>
                                <li>• Organisation de points réguliers avec le RSI pour réviser le document et confirmer sa conformité avec les attentes internes et celles de l’audit.</li>
                            </ul>
                        </p>

                <!-- projet-content-end -->

                <!-- project content continues -->
                <div>
                    <p>
                        <h5>Difficultés rencontrées</h5>
                            <ul>
                                <li><b>• Complexité du projet </b>: nécessité de concilier exigences de sécurité technique et pratiques réelles.</li>
                                <li><b>• Version obsolète de SAPANET </b>: gestion des Plans d’Action parfois fastidieuse.</li>
                                <li><b>• Exigence d’exactitude </b>: éviter de promettre des mesures non encore mises en œuvre tout en intégrant des engagements progressifs.</li>
                            </ul>
                    </p>
                    <p>
                        <h5>Solutions apportées</h5>
                            <ul>
                                <li>• <b>Communication simplifiée : </b>rédaction en langage clair pour faciliter les échanges et validations</li>
                                <li>• <b>Optimisation de SAPANET : </b>structuration rigoureuse des suivi pour garantir une traçabilité précise.</li>
                                <li>• <b>PSSI pragmatique : </b>alignée sur les pratiques actuelles, avec des engagements futurs assortis de Plans d’Action définis.</li>
                            </ul>
                    </p>
                    <p>
                        <h5>Compétences couvertes</h5>
                            <ul>
                                <li><b>• Gérer le patrimoine informatique :</b>
                                    <ul>
                                        <li>Recenser et identifier les ressources numériques</li>
                                        <li>Exploiter des référentiels, normes et standards adoptés par le prestataire informatique</li>
                                        <li>Mettre en place et vérifier les niveaux d’habilitation associés à un service</li>
                                        <li>Gérer des sauvegardes</li>
                                        <li>Vérifier le respect des règles d’utilisation des ressources numériques</li>
                                    </ul>
                                </li>
                                <li><b>• Travailler en mode projet :</b>
                                    <ul>
                                        <li>Analyser les objectifs et les modalités d'organisation d'un projet</li>
                                        <li>Planifier les activités</li>
                                        <li>Évaluer les indicateurs de suivi d'un projet et analyser les écarts</li>
                                    </ul>
                                </li>
                                <li><b>•  Organiser son développement professionnel :</b>
                                    <ul>
                                        <li>Mettre en place son environnement d’apprentissage personnel</li>
                                        <li>Mettre en œuvre des outils et stratégies de veille informationnelle</li>
                                    </ul>
                                </li>

                            </ul>
                    </p>
                    <p>
                        <p>
                        <h5>Bilan Personnel</h5>
                            <h6>Aptitudes renforcées :</h6>
                                <ul>
                                    <li>• Rédaction de documentation technique conforme à un audit externe.</li>
                                    <li>• Suivi structuré des actions via l’outil qualité SAPANET.</li>
                                    <li>• Collaboration avec le RSI, un prestataire externe et un technicien pour assurer une bonne coordination des interventions.</li>
                                </ul>
                        </p>
                        <p>
                            <h6>Axes d'amélioration :</h6>
                                <ul>
                                    <li>• Proposer un modèle de PSSI ajustable pour simplifier les révisions futures.</li>
                                    <li>• Mise à jour de SAPANET pour simplifier le suivi.</li>
                                </ul>
                        </p>
                    </p>
